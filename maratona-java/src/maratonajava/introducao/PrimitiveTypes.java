package maratonajava.introducao;

public class PrimitiveTypes {
    public static void main(String[] args) {
        int age = 10;
        long bigNumber = 100000L;
        double salaryOne = 2000.0D;
        float salaryTwo = 2500.0F;
        byte ageOne = -128;
        short ageTwo = 32000;
        boolean tru = true;
        boolean fake = false;
        char characters = 'M';
        String name = "Goku";

        System.out.println("Age is "+age+" years.");
        System.out.println(fake);
        System.out.println(tru);
        System.out.println("Gender: "+characters);
        System.out.println(bigNumber);
        System.out.println("Hi, my name is "+name+"!");
        System.out.println(salaryOne);
        System.out.println(salaryTwo);
        System.out.println(ageOne);
        System.out.println(ageTwo);
    }
}
